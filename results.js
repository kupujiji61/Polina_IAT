define(['questAPI'], function (Quest) {
    let API = new Quest();

    /********* Страница *********/
    API.addPagesSet('resultsPage', {
        noSubmit: false,
        header: 'Результаты теста скрытых ассоциаций',
        decline: false,
        autoFocus: true,
        progressBar: 'Страница <%= pagesMeta.number %> из 1',
        submitText: 'Далее'
    });

    /********* Вопрос-«заглушка» с текстом *********/
    API.addQuestionsSet('resultsInfo', {
        type: 'info',
        name: 'iat_results_info',
        autoSubmit: false,
        decline: false,

        stem: [
            '<% ',
            '  var r = {};',
            '  if (typeof global !== "undefined" && global && global.raceiat) {',
            '      r = global.raceiat;',
            '  } else if (typeof window !== "undefined" && window.global && window.global.raceiat) {',
            '      r = window.global.raceiat;',
            '  }',

            '  var dRaw = "";',
            '  if (r.d !== undefined && r.d !== null && r.d !== "") {',
            '      dRaw = r.d;',
            '  } else if (r.d_str !== undefined && r.d_str !== null && r.d_str !== "") {',
            '      dRaw = r.d_str;',
            '  } else if (r.dScore !== undefined && r.dScore !== null && r.dScore !== "") {',
            '      dRaw = r.dScore;',
            '  }',

            '  var dNum = parseFloat(String(dRaw).replace(",", "."));',
            '  var hasD = !(dRaw === "" || dRaw === null || typeof dRaw === "undefined") && !isNaN(dNum);',
            '  var dText = hasD ? dNum.toFixed(2) : "не рассчитан";',

            '  var fb = r.feedback || "";',

            '  if (!fb) {',
            '      if (!hasD) {',
            '          fb = "Недостаточно попыток для выявления результатов.";',
            '      } else if (dNum >= 0.65) {',
            '          fb = "Сильная положительная автоматическая ассоциация: реакции были заметно быстрее при сочетании Полины с положительными словами.";',
            '      } else if (dNum >= 0.35) {',
            '          fb = "Умеренная положительная автоматическая ассоциация: реакции были быстрее при сочетании Полины с положительными словами.";',
            '      } else if (dNum >= 0.15) {',
            '          fb = "Слабая положительная автоматическая ассоциация: реакции были немного быстрее при сочетании Полины с положительными словами.";',
            '      } else if (dNum > -0.15) {',
            '          fb = "Заметного перекоса в скорости реакций между сравниваемыми сочетаниями не выявлено.";',
            '      } else if (dNum > -0.35) {',
            '          fb = "Слабая отрицательная автоматическая ассоциация: реакции были немного быстрее при альтернативных сочетаниях.";',
            '      } else if (dNum > -0.65) {',
            '          fb = "Умеренная отрицательная автоматическая ассоциация: реакции были быстрее при альтернативных сочетаниях.";',
            '      } else {',
            '          fb = "Сильная отрицательная автоматическая ассоциация: реакции были заметно быстрее при альтернативных сочетаниях.";',
            '      }',
            '  }',
            '%>',

            '<div class="well" style="margin-bottom: 24px;">',
            '  <p class="lead"><b>Ваш результат</b></p>',
            '  <p><b>D-score:</b> <span style="font-size: 22px; font-weight: bold;"><%= dText %></span></p>',
            '  <p><b>Интерпретация:</b> <%= fb %></p>',
            '  <p style="font-size: 14px; color: #666;">',
            '      Важно: результат отражает скорость реакций в этом прохождении теста и не является психологической диагностикой.',
            '  </p>',
            '</div>',

            '<p class="lead"><b>Что измеряет этот тест?</b></p>',

            '<p>Тест имплицитных ассоциаций (Implicit Association Test, IAT) измеряет скорость, с которой человек ',
            'соотносит разные категории, например Полину и «хорошее», со словами и картинками.</p>',

            '<p>Предполагается, что чем быстрее вы реагируете, когда вместе появляются «согласующиеся» для вас ',
            'сочетания, например Полина + приятные слова, тем сильнее автоматическая связь между ними.</p>',

            '<p>Положительное значение D-score означает более быструю реакцию при сочетании Полины с «хорошим» ',
            'по сравнению с альтернативными сочетаниями. Отрицательное значение — наоборот.</p>',

            '<p>Чем больше по модулю значение D-score, тем сильнее выражена ассоциация. Значения, близкие к 0, ',
            'показывают, что заметного перекоса в скорости реакций между сравниваемыми парами нет.</p>'
        ].join('')
    });

    /********* Последовательность *********/
    API.addSequence([
        {
            inherit: 'resultsPage',
            questions: { inherit: 'resultsInfo' }
        }
    ]);

    return API.script;
});
